public class Calcolatrice{
  int a;
  int b;
  int sum(int a, int b){
    return a + b;
  }

  int sottrazione(int a, int b){
    return a-b;
  }

  int moltiplicazione(int a, int b){
    return a*b;
  }

  int divisione(int a, int b){
    return a/b;
    
  }
  int rimanenza(int a, int b){
    return a%b;
  }
}